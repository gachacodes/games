        const gridAnsicht = document.getElementById("ansicht-grid");
        const spielAnsicht = document.getElementById("ansicht-spiel");
        const bagel_text = document.getElementById("bagel");
        
        const imgElement = document.getElementById("suchbild");
        const aufgabeElement = document.getElementById("aufgabe");
        const loesungElement = document.getElementById("loesung");
        
        const btnLoesen = document.getElementById("btn-loesen");
        const btnZurueck = document.getElementById("btn-zurueck");

        let loesungSichtbar = false;

        bagel_text.addEventListener("click", () => {
    
    const idZumKopieren = bagel_text.dataset.id;
    
    
    navigator.clipboard.writeText(idZumKopieren).then(() => {
        
        const originalText = bagel_text.innerText;
        
        
        bagel_text.innerText = "Copied!";
        bagel_text.style.color = "#28a745"; 
        
        
        setTimeout(() => {
            bagel_text.innerText = originalText;
            bagel_text.style.color = ""; 
        }, 1500);
    }).catch(err => {
        console.error("Error while trying to copy: ", err);
        alert("Error while copying!");
    });
});

        
        function generiereGrid() {
            gridAnsicht.innerHTML = ""; 
            
            bilderDaten.forEach((daten, index) => {
                
                const item = document.createElement("div");
                item.className = "grid-item";
                
                
                item.onclick = () => starteSpiel(index);
                
                
                item.innerHTML = `
                    <img src="${daten.src}" alt="Vorschau">
                    <div class="grid-text">${daten.frage}</div>
                `;
                
                gridAnsicht.appendChild(item);
            });
        }

        
        function starteSpiel(index) {
            const daten = bilderDaten[index];
            
            bagel_text.style.display = "block";
            gridAnsicht.style.display = "none";
            spielAnsicht.style.display = "block";
            
            bagel.innerText = "Bagel-ID: "+daten.bagel_id;
            bagel_text.dataset.id = daten.bagel_id;
            imgElement.src = daten.src;
            aufgabeElement.innerText = daten.frage;

            loesungElement.style.left = daten.x + "%";
            loesungElement.style.top = daten.y + "%";
            loesungElement.style.width = daten.breite + "%";
            loesungElement.style.height = daten.hoehe + "%";

            setzeLoesungZustand(false);
            
        }

        
        btnZurueck.addEventListener("click", () => {
            bagel_text.style.display = "none";
            spielAnsicht.style.display = "none";
            gridAnsicht.style.display = "grid"; 
            
            
            imgElement.src = "";
            setzeLoesungZustand(false);
        });

        function setzeLoesungZustand(sichtbar) {
            loesungSichtbar = sichtbar;
            
            if (loesungSichtbar) {
                loesungElement.style.display = "block";
                btnLoesen.innerText = "Hide";
                btnLoesen.classList.add("active");
            }else{ 
                loesungElement.style.display = "none";
                btnLoesen.innerText = "Where is Mint?";
                btnLoesen.classList.remove("active");
            }
        }

        btnLoesen.addEventListener("click", () => {
            setzeLoesungZustand(!loesungSichtbar);
            
        });

        generiereGrid();